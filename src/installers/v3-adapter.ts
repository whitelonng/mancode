/** Computes the content digest for one logical managed target. */
export function adapterManagedContentDigest(
  targetIdentity: string,
  managedContent: string | Uint8Array,
): string {
  if (!targetIdentity.trim() || targetIdentity.includes('\0')) {
    throw new Error('MANCODE_V3_ADAPTER_TARGET_IDENTITY_INVALID');
  }
  const content =
    typeof managedContent === 'string'
      ? Buffer.from(managedContent, 'utf8')
      : Buffer.from(managedContent);
  const digest = createHash('sha256')
    .update(Buffer.from(V3_ADAPTER_DIGEST_DOMAIN, 'utf8'))
    .update(Buffer.from([0]))
    .update(Buffer.from(targetIdentity, 'utf8'))
    .update(Buffer.from([0]))
    .update(content)
    .digest('hex');
  return `sha256:${digest}`;
}

/**
 * Calculates exact file replacements without publishing them. This lets a
 * migration journal bind the combined AGENTS.md result before its first
 * visible write and preserves all user-authored content outside our blocks.
 */
export async function planV3AdapterFiles(
  projectRoot: string,
): Promise<V3AdapterFilePlan[]> {
  const root = path.resolve(projectRoot);
  const existing = new Map<V3AdapterFileTarget, string | null>();
  for (const target of V3_ADAPTER_FILE_TARGETS) {
    existing.set(target, await readAdapterTarget(root, target));
  }
  const agents = removeLegacyV3Block(
    removeLegacyV3Block(
      removeLegacyAgentsBlocks(existing.get('agents') ?? ''),
      LEGACY_V3_CODEX_MARKERS,
    ),
    LEGACY_V3_ZCODE_MARKERS,
  );
  const nextAgents = replaceManagedV3BlockText(
    replaceManagedV3BlockText(
      agents,
      V3_CODEX_START_MARKER,
      V3_CODEX_END_MARKER,
      renderV3Bootstrap('codex'),
    ),
    V3_ZCODE_START_MARKER,
    V3_ZCODE_END_MARKER,
    renderV3Bootstrap('zcode'),
  );
  const legacyAdapterPlans = planLegacyAdapterRetirement(existing);
  const plans: V3AdapterFilePlan[] = [
    {
      target: 'claude-skill',
      beforeContent: existing.get('claude-skill') ?? null,
      targetContent: replaceManagedV3BlockText(
        existing.get('claude-skill') ?? '',
        CONTINUITY_CLAUDE_START_MARKER,
        CONTINUITY_CLAUDE_END_MARKER,
        renderV3Bootstrap('claude-code'),
      ),
    },
    managedFilePlan(
      'cursor-rule',
      existing.get('cursor-rule') ?? null,
      renderCursorRule(renderV3Bootstrap('cursor')),
    ),
    {
      target: 'agents',
      beforeContent: existing.get('agents') ?? null,
      targetContent: nextAgents,
    },
    {
      target: 'copilot-instructions',
      beforeContent: existing.get('copilot-instructions') ?? null,
      targetContent: replaceManagedV3BlockText(
        removeLegacyV3Block(
          removeManagedBlock(existing.get('copilot-instructions') ?? ''),
          LEGACY_V3_COPILOT_MARKERS,
        ),
        V3_COPILOT_START_MARKER,
        V3_COPILOT_END_MARKER,
        renderV3Bootstrap('copilot'),
      ),
    },
    ...V3_MODE_ENTRY_FILE_TARGETS.map((target) =>
      managedModeEntryPlan(
        target,
        existing.get(target) ?? null,
        renderModeEntryForFileTarget(target),
      ),
    ),
    ...legacyAdapterPlans,
  ];
  return plans;
}

/** Plans a selected-platform repair while composing shared AGENTS targets once. */
export async function planV3AdapterUpgradeFiles(
  projectRoot: string,
  platforms: readonly PlatformName[],
): Promise<V3AdapterFilePlan[]> {
  const root = path.resolve(projectRoot);
  const selected = normalizeUpgradePlatforms(platforms);
  const targetSet = new Set<V3AdapterFileTarget>();
  for (const platform of selected) {
    targetSet.add(primaryFileTarget(platform));
    for (const mode of V3_MODE_NAMES) {
      targetSet.add(modeEntryFileTarget(platform, mode));
    }
    for (const target of legacyAdapterTargetsForPlatform(platform, true)) {
      targetSet.add(target);
    }
  }
  const existing = new Map<V3AdapterFileTarget, string | null>();
  for (const target of targetSet) {
    existing.set(target, await readAdapterTarget(root, target));
  }
  const desired = new Map(existing);
  for (const platform of selected) {
    planPlatformBootstrapUpgrade(desired, platform);
    for (const mode of V3_MODE_NAMES) {
      const target = modeEntryFileTarget(platform, mode);
      const current = desired.get(target) ?? null;
      desired.set(
        target,
        managedModeEntryPlan(target, current, renderV3ModeEntry(mode, platform))
          .targetContent,
      );
    }
  }
  const plans = [...desired.entries()]
    .filter(([target, targetContent]) => existing.get(target) !== targetContent)
    .map(([target, targetContent]) => ({
      target,
      beforeContent: existing.get(target) ?? null,
      targetContent: targetContent ?? '',
    }));
  const legacyPlans = planLegacyAdapterRetirement(existing).filter(
    (legacyPlan) =>
      !plans.some((candidate) => candidate.target === legacyPlan.target),
  );
  return [...plans, ...legacyPlans];
}

/** Writes immutable upgrade candidates below .mancode staging, never live targets. */
export async function stageV3AdapterUpgradeFiles(
  projectRoot: string,
  operationId: string,
  plans: readonly V3AdapterFilePlan[],
): Promise<V3StagedAdapterFilePlan[]> {
  if (!/^[0-7][0-9A-HJKMNP-TV-Z]{25}$/.test(operationId)) {
    throw new Error('MANCODE_ADAPTER_UPGRADE_OPERATION_ID_INVALID');
  }
  const root = path.resolve(projectRoot);
  const staged: V3StagedAdapterFilePlan[] = [];
  for (const plan of plans) {
    const liveTarget = v3AdapterTargetPath(root, plan.target);
    const relative = relativeAdapterPath(root, liveTarget);
    const stagingTarget = path.join(
      '.mancode',
      'staging',
      'adapters',
      'upgrade',
      operationId,
      relative,
    );
    const destination = path.join(root, stagingTarget);
    await assertAdapterPathSafe(root, destination);
    await mkdir(path.dirname(destination), { recursive: true });
    await atomicWrite(destination, plan.targetContent);
    staged.push({
      target: plan.target,
      stagingTarget: relativeAdapterPath(root, destination),
    });
  }
  return staged;
}

/** Publishes one precomputed fixed-target replacement with atomic rename. */
export async function applyV3AdapterFilePlan(
  projectRoot: string,
  plan: V3AdapterFilePlan,
): Promise<void> {
  const root = path.resolve(projectRoot);
  if (!V3_ADAPTER_FILE_TARGETS.includes(plan.target)) {
    throw new Error('MANCODE_V3_ADAPTER_TARGET_INVALID');
  }
  if (typeof plan.targetContent !== 'string' || !plan.targetContent.trim()) {
    throw new Error('MANCODE_V3_ADAPTER_TARGET_INVALID');
  }
  const target = v3AdapterTargetPath(root, plan.target);
  await assertAdapterPathSafe(root, target);
  const retiredBootstrapPlatform = retiredBootstrapPlatformFor(plan.target);
  if (retiredBootstrapPlatform !== null) {
    for (const retired of retiredBootstrapSpecs(
      root,
      retiredBootstrapPlatform,
    )) {
      await assertAdapterPathSafe(root, retired.filePath);
    }
  }
  const current = await readAdapterTarget(root, plan.target);
  if (current === plan.targetContent) {
    if (retiredBootstrapPlatform !== null) {
      await removeRetiredBootstrapFiles(root, retiredBootstrapPlatform);
    }
    return;
  }
  if (current !== plan.beforeContent) {
    throw new Error('MANCODE_V3_ADAPTER_TARGET_CONFLICT');
  }
  await mkdir(path.dirname(target), { recursive: true });
  await atomicWrite(target, plan.targetContent);
  if (retiredBootstrapPlatform !== null) {
    // The old bootstrap is non-authoritative and is retired only after its
    // Continuity replacement is durable. A later repair safely retries this.
    await removeRetiredBootstrapFiles(root, retiredBootstrapPlatform);
  }
}

/**
 * Renders a complete adapter candidate under Continuity staging. Shadow integration
 * may inspect this exact replacement without changing a live managed file.
 */
export async function stageV3Adapter(
  projectRoot: string,
  platform: PlatformName,
): Promise<V3StagedAdapter> {
  const root = path.resolve(projectRoot);
  await assertPlatformAdapterPathsSafe(root, platform);
  const target = targetFor(platform);
  const content = await renderV3AdapterCandidate(root, platform);
  const stagingTarget = path.join(
    '.mancode',
    'staging',
    'adapters',
    'continuity',
    platform,
    target,
  );
  const destination = path.join(root, stagingTarget);
  await assertAdapterPathSafe(root, destination);
  await mkdir(path.dirname(destination), { recursive: true });
  await atomicWrite(destination, content);
  const modeEntries = [] as V3StagedAdapter['modeEntries'];
  for (const mode of V3_MODE_NAMES) {
    const modeTarget = path.relative(
      root,
      v3ModeEntryPath(root, platform, mode),
    );
    const modeStagingTarget = path.join(
      '.mancode',
      'staging',
      'adapters',
      'continuity',
      platform,
      modeTarget,
    );
    const modeDestination = path.join(root, modeStagingTarget);
    await assertAdapterPathSafe(root, modeDestination);
    await mkdir(path.dirname(modeDestination), { recursive: true });
    await atomicWrite(modeDestination, renderV3ModeEntry(mode, platform));
    modeEntries.push({
      mode,
      target: modeTarget,
      stagingTarget: modeStagingTarget,
    });
  }
  return { platform, target, stagingTarget, modeEntries };
}

export function v3AdapterTargetPath(
  projectRoot: string,
  target: V3AdapterFileTarget,
): string {
  const root = path.resolve(projectRoot);
  const modeTarget = parseModeEntryFileTarget(target);
  if (modeTarget !== null) {
    return v3ModeEntryPath(root, modeTarget.platform, modeTarget.mode);
  }
  const legacyTarget = legacyAdapterRelativePath(target);
  if (legacyTarget !== null) return path.join(root, legacyTarget);
  switch (target) {
    case 'claude-skill':
      return path.join(root, 'CLAUDE.md');
    case 'cursor-rule':
      return path.join(root, '.cursor', 'rules', 'mancode-continuity.mdc');
    case 'agents':
      return path.join(root, 'AGENTS.md');
    case 'copilot-instructions':
      return path.join(root, '.github', 'copilot-instructions.md');
  }
  throw new Error('MANCODE_V3_ADAPTER_TARGET_INVALID');
}

/** Validates every existing segment before a recovery read or removal. */
export async function assertV3AdapterTargetSafe(
  projectRoot: string,
  target: V3AdapterFileTarget,
): Promise<void> {
  const root = path.resolve(projectRoot);
  await assertAdapterPathSafe(root, v3AdapterTargetPath(root, target));
}

/** The original public mode name, rendered at the host's native entry path. */
export function v3ModeEntryPath(
  projectRoot: string,
  platform: PlatformName,
  mode: V3ModeName,
): string {
  const root = path.resolve(projectRoot);
  switch (platform) {
    case 'claude-code':
      return path.join(root, '.claude', 'skills', mode, 'SKILL.md');
    case 'codex':
    case 'zcode':
      return path.join(root, '.agents', 'skills', mode, 'SKILL.md');
    case 'cursor':
      return path.join(root, '.cursor', 'commands', `${mode}.md`);
    case 'copilot':
      return path.join(root, '.github', 'prompts', `${mode}.prompt.md`);
  }
}

/**
 * Writes only stable V3 bootstrap instructions.  In particular, this never
 * creates legacy authority or copies task state into an adapter file.
 */
export async function installV3Adapter(
  projectRoot: string,
  platform: PlatformName,
): Promise<V3PlatformAdapterStatus> {
  const root = path.resolve(projectRoot);
  await assertV3AdapterInstallable(root, platform);
  const content = renderV3Bootstrap(platform);
  switch (platform) {
    case 'claude-code':
      await replaceManagedV3Block(
        path.join(root, 'CLAUDE.md'),
        CONTINUITY_CLAUDE_START_MARKER,
        CONTINUITY_CLAUDE_END_MARKER,
        content,
      );
      await removeRetiredBootstrapFiles(root, platform);
      break;
    case 'cursor':
      await writeManagedFile(
        path.join(root, '.cursor', 'rules', 'mancode-continuity.mdc'),
        renderCursorRule(content),
      );
      await removeRetiredBootstrapFiles(root, platform);
      break;
    case 'codex':
      await replaceManagedV3Block(
        path.join(root, 'AGENTS.md'),
        V3_CODEX_START_MARKER,
        V3_CODEX_END_MARKER,
        content,
        [
          LEGACY_V3_CODEX_MARKERS,
          [LEGACY_CODEX_START_MARKER, LEGACY_CODEX_END_MARKER],
          [LEGACY_ZCODE_START_MARKER, LEGACY_ZCODE_END_MARKER],
        ],
      );
      break;
    case 'copilot':
      await replaceManagedV3Block(
        path.join(root, '.github', 'copilot-instructions.md'),
        V3_COPILOT_START_MARKER,
        V3_COPILOT_END_MARKER,
        content,
        [
          LEGACY_V3_COPILOT_MARKERS,
          [LEGACY_CODEX_START_MARKER, LEGACY_CODEX_END_MARKER],
        ],
      );
      break;
    case 'zcode':
      await replaceManagedV3Block(
        path.join(root, 'AGENTS.md'),
        V3_ZCODE_START_MARKER,
        V3_ZCODE_END_MARKER,
        content,
        [
          LEGACY_V3_ZCODE_MARKERS,
          [LEGACY_CODEX_START_MARKER, LEGACY_CODEX_END_MARKER],
          [LEGACY_ZCODE_START_MARKER, LEGACY_ZCODE_END_MARKER],
        ],
      );
      break;
  }
  await retireLegacyPlatformFiles(root, platform);
  for (const mode of V3_MODE_NAMES) {
    await writeV3ModeEntry(
      v3ModeEntryPath(root, platform, mode),
      renderV3ModeEntry(mode, platform),
    );
  }
  return inspectV3Adapter(root, platform);
}

/**
 * Proves all deterministic adapter writes are safe before initialization or
 * repair publishes its first file. I/O can still fail later, but user-owned
 * targets, malformed managed settings, and unsafe paths cannot leave a known
 * partial install behind.
 */
export async function assertV3AdapterInstallable(
  projectRoot: string,
  platform: PlatformName,
): Promise<void> {
  const root = path.resolve(projectRoot);
  await assertPlatformAdapterPathsSafe(root, platform);
  await assertV3ModeEntriesWritable(root, platform);
  await renderV3AdapterCandidate(root, platform);

  const existing = new Map<V3AdapterFileTarget, string | null>();
  for (const target of legacyAdapterTargetsForPlatform(platform, true)) {
    existing.set(target, await readAdapterTarget(root, target));
  }
  planLegacyAdapterRetirement(existing);
}

/** Physical adapter status intentionally does not infer hook approval. */
export async function inspectV3Adapter(
  projectRoot: string,
  platform: PlatformName,
): Promise<V3PlatformAdapterStatus> {
  const root = path.resolve(projectRoot);
  const target = targetFor(platform);
  const targets: V3AdapterManagedTargetStatus[] = [];
  for (const spec of managedTargetSpecs(root, platform)) {
    targets.push(await inspectManagedTarget(root, platform, spec));
  }
  const ready = targets.every((item) => item.status === 'ready');
  const installed = targets.every((item) => item.status !== 'missing');
  const status = aggregateAdapterStatus(targets);
  return {
    version: V3_ADAPTER_VERSION,
    installed,
    ready,
    status,
    target,
    detail:
      status === 'ready'
        ? 'Managed adapter content matches the current renderer.'
        : status === 'missing'
          ? 'One or more managed adapter targets are missing.'
          : status === 'stale'
            ? 'Managed adapter content differs from the current renderer.'
            : 'One or more managed adapter targets cannot be read safely.',
    targets,
    capabilities: capabilitiesFor(platform),
  };
}

