function planLegacyAdapterRetirement(
  existing: ReadonlyMap<V3AdapterFileTarget, string | null>,
): V3AdapterFilePlan[] {
  const plans: V3AdapterFilePlan[] = [];
  const settings = existing.get('claude-settings') ?? null;
  if (settings !== null) {
    const cleaned = removeLegacyClaudeSettings(settings);
    if (cleaned !== settings) {
      plans.push({
        target: 'claude-settings',
        beforeContent: settings,
        targetContent: cleaned,
      });
    }
  }

  const legacySolo = existing.get('claude-legacy-solo') ?? null;
  if (
    legacySolo?.includes(
      '<!-- Managed by mancode:claude-skill. Do not edit this marker. -->',
    )
  ) {
    plans.push({
      target: 'claude-legacy-solo',
      beforeContent: legacySolo,
      targetContent: renderRetiredClaudeSoloEntry(),
    });
  }

  for (const target of V3_LEGACY_ADAPTER_FILE_TARGETS) {
    if (!target.startsWith('cursor-legacy-')) continue;
    const beforeContent = existing.get(target) ?? null;
    if (
      beforeContent?.includes(
        '<!-- Managed by mancode:cursor-rule. Do not edit this marker. -->',
      )
    ) {
      plans.push({
        target,
        beforeContent,
        targetContent: renderRetiredCursorRule(target),
      });
    }
  }
  for (const target of V3_LEGACY_ADAPTER_FILE_TARGETS) {
    const alias = parseLegacyModeAliasTarget(target);
    if (alias === null) continue;
    const beforeContent = existing.get(target) ?? null;
    if (
      beforeContent !== null &&
      isGeneratedLegacyModeAlias(beforeContent, alias.alias)
    ) {
      plans.push({
        target,
        beforeContent,
        targetContent: renderRetiredModeAlias(target),
      });
    }
  }
  return plans;
}

async function retireLegacyPlatformFiles(
  root: string,
  platform: PlatformName,
): Promise<void> {
  const targets = legacyAdapterTargetsForPlatform(platform, true);
  if (targets.length === 0) return;
  const existing = new Map<V3AdapterFileTarget, string | null>();
  for (const target of targets) {
    existing.set(target, await readAdapterTarget(root, target));
  }
  for (const plan of planLegacyAdapterRetirement(existing)) {
    await applyV3AdapterFilePlan(root, plan);
  }
}

async function removeRetiredLegacyPlatformFiles(
  root: string,
  platform: PlatformName,
): Promise<void> {
  const targets = legacyAdapterTargetsForPlatform(platform, false);
  for (const target of targets) {
    const filePath = v3AdapterTargetPath(root, target);
    const content = await readTextIfExists(filePath);
    const retiredModeEntry =
      target === 'claude-legacy-solo' ||
      parseLegacyModeAliasTarget(target) !== null;
    const retired = content?.includes(
      retiredModeEntry
        ? V3_MODE_ENTRY_MANAGED_MARKER
        : V3_ADAPTER_MANAGED_MARKER,
    );
    if (retired) {
      await rm(filePath, { force: true });
      if (
        target === 'claude-legacy-solo' ||
        target.startsWith('claude-alias-') ||
        target.startsWith('agents-alias-')
      ) {
        await removeDirectoryIfEmpty(path.dirname(filePath));
      }
    }
  }
}

function legacyAdapterTargetsForPlatform(
  platform: PlatformName,
  includeSettings: boolean,
): V3LegacyAdapterFileTarget[] {
  if (platform === 'claude-code') {
    return [
      ...(includeSettings ? (['claude-settings'] as const) : []),
      'claude-legacy-solo',
      ...V3_LEGACY_ADAPTER_FILE_TARGETS.filter((target) =>
        target.startsWith('claude-alias-'),
      ),
    ];
  }
  if (platform === 'cursor') {
    return V3_LEGACY_ADAPTER_FILE_TARGETS.filter((target) =>
      /^(cursor-legacy|cursor-alias)-/u.test(target),
    );
  }
  if (platform === 'codex' || platform === 'zcode') {
    return V3_LEGACY_ADAPTER_FILE_TARGETS.filter((target) =>
      target.startsWith('agents-alias-'),
    );
  }
  return V3_LEGACY_ADAPTER_FILE_TARGETS.filter((target) =>
    target.startsWith('copilot-alias-'),
  );
}

function removeLegacyClaudeSettings(content: string): string {
  const hasKnownLegacyReference = LEGACY_CLAUDE_SETTINGS_RAW_HINTS.some(
    (value) => content.includes(value),
  );
  if (!hasKnownLegacyReference) return content;
  let parsed: unknown;
  try {
    parsed = JSON.parse(content);
  } catch {
    throw new Error('MANCODE_V3_CLAUDE_SETTINGS_INVALID');
  }
  if (!isRecord(parsed)) {
    throw new Error('MANCODE_V3_CLAUDE_SETTINGS_INVALID');
  }
  const hasGeneratedHook = containsGeneratedClaudeHookValue(parsed.hooks);
  const hasGeneratedSkill =
    isRecord(parsed.skills) &&
    Object.values(parsed.skills).some(
      (value) =>
        typeof value === 'string' && LEGACY_CLAUDE_SKILL_PATHS.has(value),
    );
  if (!hasGeneratedHook && !hasGeneratedSkill) return content;
  const settings: Record<string, unknown> = { ...parsed };
  if ('hooks' in settings) {
    const hooks = removeGeneratedClaudeHookValue(settings.hooks);
    if (hooks === undefined) settings.hooks = undefined;
    else settings.hooks = hooks;
  }
  if (isRecord(settings.skills)) {
    const skills = Object.fromEntries(
      Object.entries(settings.skills).filter(
        ([, value]) =>
          typeof value !== 'string' || !LEGACY_CLAUDE_SKILL_PATHS.has(value),
      ),
    );
    if (Object.keys(skills).length === 0) settings.skills = undefined;
    else settings.skills = skills;
  }
  return `${JSON.stringify(settings, null, 2)}\n`;
}

function containsGeneratedClaudeHookValue(value: unknown): boolean {
  if (Array.isArray(value)) {
    return value.some(containsGeneratedClaudeHookValue);
  }
  if (!isRecord(value)) return false;
  if (
    typeof value.command === 'string' &&
    isGeneratedClaudeHookCommand(value.command)
  ) {
    return true;
  }
  return Object.values(value).some(containsGeneratedClaudeHookValue);
}

function removeGeneratedClaudeHookValue(value: unknown): unknown | undefined {
  if (Array.isArray(value)) {
    const cleaned = value
      .map(removeGeneratedClaudeHookValue)
      .filter((entry) => entry !== undefined);
    return cleaned.length > 0 ? cleaned : undefined;
  }
  if (!isRecord(value)) return value;
  if (
    typeof value.command === 'string' &&
    isGeneratedClaudeHookCommand(value.command)
  ) {
    return undefined;
  }
  if (Array.isArray(value.hooks)) {
    const hooks = value.hooks
      .map(removeGeneratedClaudeHookValue)
      .filter((entry) => entry !== undefined);
    return hooks.length > 0 ? { ...value, hooks } : undefined;
  }
  const cleaned = Object.entries(value).flatMap(([key, entry]) => {
    const next = removeGeneratedClaudeHookValue(entry);
    return next === undefined ? [] : [[key, next] as const];
  });
  return cleaned.length > 0 ? Object.fromEntries(cleaned) : undefined;
}

function isGeneratedClaudeHookCommand(command: string): boolean {
  return GENERATED_CLAUDE_HOOK_COMMANDS.has(command.trim());
}

function renderRetiredClaudeSoloEntry(): string {
  return [
    '---',
    'name: solo',
    'description: "Compatibility alias for the mancode mansolo entry."',
    'user-invocable: false',
    '---',
    '',
    V3_MODE_ENTRY_MANAGED_MARKER,
    '',
    '# mancode mode compatibility alias',
    '',
    'Use the `mansolo` mode entry. Resolve mancode status, identity, session, TaskRef, and Context Pack there; do not use legacy mode persistence.',
    '',
  ].join('\n');
}

function renderRetiredCursorRule(target: V3LegacyAdapterFileTarget): string {
  const legacyName = target.replace('cursor-legacy-', '');
  const mode =
    legacyName === 'solo'
      ? 'mansolo'
      : legacyName === 'mamba'
        ? 'manba'
        : legacyName === 'man8'
          ? 'man'
          : legacyName;
  const guidance =
    mode === 'context' || mode === 'practice'
      ? 'Run `mancode status --brief --json`, then use the matching mancode mode command.'
      : `Use the \`/${mode}\` mancode mode command.`;
  return [
    '---',
    'description: "Retired legacy mancode rule; current mancode mode entries are authoritative."',
    'alwaysApply: false',
    'globs: "__mancode_v3_retired_rule__"',
    '---',
    '',
    V3_ADAPTER_MANAGED_MARKER,
    '',
    '# mancode compatibility redirect',
    '',
    guidance,
    'Do not use legacy mode persistence or legacy workflow paths.',
    '',
  ].join('\n');
}

function renderRetiredModeAlias(target: V3AdapterFileTarget): string {
  const parsed = parseLegacyModeAliasTarget(target);
  if (parsed === null) {
    throw new Error('MANCODE_V3_ADAPTER_TARGET_INVALID');
  }
  const publicMode = parsed.alias === 'mamba' ? 'manba' : 'man';
  const frontmatter = [
    '---',
    ...(parsed.family === 'claude' || parsed.family === 'agents'
      ? [`name: ${parsed.alias}`]
      : []),
    ...(parsed.family === 'copilot' ? ["agent: 'agent'"] : []),
    ...(parsed.family === 'claude' ? ['user-invocable: false'] : []),
    `description: ${JSON.stringify(`Compatibility alias for the mancode ${publicMode} entry.`)}`,
    '---',
  ];
  return [
    ...frontmatter,
    '',
    V3_MODE_ENTRY_MANAGED_MARKER,
    '',
    '# mancode mode compatibility alias',
    '',
    `The legacy name \`${parsed.alias}\` maps to the public mancode mode \`${publicMode}\`. Use that original mancode mode entry instead.`,
    '',
    'Resolve status, identity, session, TaskRef, and Context Pack through mancode there. Do not use legacy mode persistence or the legacy workflow protocol.',
    '',
  ].join('\n');
}

function isGeneratedLegacyModeAlias(
  content: string,
  alias: LegacyModeAlias,
): boolean {
  if (
    content.includes(V3_MODE_ENTRY_MANAGED_MARKER) ||
    LEGACY_MODE_ENTRY_MANAGED_MARKERS.some((marker) => content.includes(marker))
  ) {
    return true;
  }
  if (alias !== 'man8') return false;
  return (
    content.includes('# mancode · /man8 (4 AM Warmup)') ||
    (content.includes('# mancode man8 — Investigate and Plan') &&
      content.includes('## Mode Persistence'))
  );
}

function legacyAdapterRelativePath(target: V3AdapterFileTarget): string | null {
  if (target === 'claude-settings')
    return path.join('.claude', 'settings.json');
  if (target === 'claude-legacy-solo') {
    return path.join('.claude', 'skills', 'solo', 'SKILL.md');
  }
  if (target.startsWith('cursor-legacy-')) {
    const name = target.slice('cursor-legacy-'.length);
    return path.join('.cursor', 'rules', `mancode-${name}.mdc`);
  }
  const alias = parseLegacyModeAliasTarget(target);
  if (alias === null) return null;
  switch (alias.family) {
    case 'claude':
      return path.join('.claude', 'skills', alias.alias, 'SKILL.md');
    case 'agents':
      return path.join('.agents', 'skills', alias.alias, 'SKILL.md');
    case 'cursor':
      return path.join('.cursor', 'commands', `${alias.alias}.md`);
    case 'copilot':
      return path.join('.github', 'prompts', `${alias.alias}.prompt.md`);
  }
}

function parseLegacyModeAliasTarget(target: V3AdapterFileTarget): {
  family: 'claude' | 'agents' | 'cursor' | 'copilot';
  alias: LegacyModeAlias;
} | null {
  const match = /^(claude|agents|cursor|copilot)-alias-(mamba|man8)$/u.exec(
    target,
  );
  if (!match) return null;
  return {
    family: match[1] as 'claude' | 'agents' | 'cursor' | 'copilot',
    alias: match[2] as LegacyModeAlias,
  };
}

function renderModeEntryForFileTarget(target: V3ModeEntryFileTarget): string {
  const parsed = parseModeEntryFileTarget(target);
  if (parsed === null) {
    throw new Error('MANCODE_V3_ADAPTER_TARGET_INVALID');
  }
  return renderV3ModeEntry(parsed.mode, parsed.platform);
}

function parseModeEntryFileTarget(
  target: V3AdapterFileTarget,
): { platform: PlatformName; mode: V3ModeName } | null {
  const match =
    /^(claude|agents|cursor|copilot)-mode-(manba|man|manteam|manps|mansolo)$/u.exec(
      target,
    );
  if (!match) return null;
  const family = match[1];
  const mode = match[2] as V3ModeName;
  const platform: PlatformName =
    family === 'claude'
      ? 'claude-code'
      : family === 'agents'
        ? 'codex'
        : family === 'cursor'
          ? 'cursor'
          : 'copilot';
  return { platform, mode };
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === 'object' && value !== null && !Array.isArray(value);
}

async function readAdapterTarget(
  root: string,
  target: V3AdapterFileTarget,
): Promise<string | null> {
  const filePath = v3AdapterTargetPath(root, target);
  await assertAdapterPathSafe(root, filePath);
  try {
    const entry = await lstat(filePath);
    if (!entry.isFile() || entry.isSymbolicLink()) {
      throw new Error('MANCODE_ARTIFACT_PATH_UNSAFE');
    }
  } catch (error) {
    if (isNodeError(error) && error.code === 'ENOENT') return null;
    throw error;
  }
  return readFile(filePath, 'utf8');
}

async function assertPlatformAdapterPathsSafe(
  root: string,
  platform: PlatformName,
): Promise<void> {
  const targets = new Set<string>([
    path.join(root, targetFor(platform)),
    ...V3_MODE_NAMES.map((mode) => v3ModeEntryPath(root, platform, mode)),
    ...legacyAdapterTargetsForPlatform(platform, true).map((target) =>
      v3AdapterTargetPath(root, target),
    ),
  ]);
  if (platform === 'claude-code' || platform === 'cursor') {
    for (const retired of retiredBootstrapSpecs(root, platform)) {
      targets.add(retired.filePath);
    }
  }
  for (const target of targets) {
    await assertAdapterPathSafe(root, target);
  }
}

/** Rejects a symlink or non-directory in any fixed adapter path segment. */
async function assertAdapterPathSafe(
  root: string,
  target: string,
): Promise<void> {
  const relative = path.relative(root, target);
  if (!relative || relative.startsWith('..') || path.isAbsolute(relative)) {
    throw new Error('MANCODE_ARTIFACT_PATH_UNSAFE');
  }
  const rootEntry = await lstat(root);
  if (!rootEntry.isDirectory() || rootEntry.isSymbolicLink()) {
    throw new Error('MANCODE_ARTIFACT_PATH_UNSAFE');
  }
  const segments = relative.split(path.sep);
  let current = root;
  for (let index = 0; index < segments.length; index += 1) {
    current = path.join(current, segments[index] ?? '');
    try {
      const entry = await lstat(current);
      if (
        entry.isSymbolicLink() ||
        (index < segments.length - 1 && !entry.isDirectory())
      ) {
        throw new Error('MANCODE_ARTIFACT_PATH_UNSAFE');
      }
    } catch (error) {
      if (isNodeError(error) && error.code === 'ENOENT') return;
      throw error;
    }
  }
}

async function removeManagedV3Block(
  filePath: string,
  startMarker: string,
  endMarker: string,
): Promise<void> {
  const existing = await readTextIfExists(filePath);
  if (existing === null || !hasManagedBlock(existing, startMarker, endMarker)) {
    return;
  }
  const cleaned = removeManagedBlock(existing, startMarker, endMarker);
  if (cleaned.trim()) {
    await atomicWrite(filePath, `${cleaned.trimEnd()}\n`);
  } else {
    await rm(filePath, { force: true });
  }
}

async function anyManagedBlockPresent(
  filePath: string,
  markerPairs: readonly (readonly [string, string])[],
): Promise<boolean> {
  const content = await readTextIfExists(filePath);
  return (
    content !== null &&
    markerPairs.some(([startMarker, endMarker]) =>
      hasManagedBlock(content, startMarker, endMarker),
    )
  );
}

async function readAdapterBytesIfExists(
  root: string,
  filePath: string,
): Promise<Buffer | null> {
  await assertAdapterPathSafe(root, filePath);
  try {
    const entry = await lstat(filePath);
    if (!entry.isFile() || entry.isSymbolicLink()) {
      throw new Error('MANCODE_ARTIFACT_PATH_UNSAFE');
    }
  } catch (error) {
    if (isNodeError(error) && error.code === 'ENOENT') return null;
    throw error;
  }
  for (let attempt = 1; attempt <= ADAPTER_READ_MAX_ATTEMPTS; attempt += 1) {
    try {
      return await readFile(filePath);
    } catch (error) {
      if (isNodeError(error) && error.code === 'ENOENT') return null;
      if (
        !isRetriableAdapterReadError(error) ||
        attempt === ADAPTER_READ_MAX_ATTEMPTS
      ) {
        throw error;
      }
      await delay(ADAPTER_READ_RETRY_DELAY_MS * attempt);
    }
  }
  throw new Error('MANCODE_V3_ADAPTER_READ_RETRY_EXHAUSTED');
}

async function readTextIfExists(filePath: string): Promise<string | null> {
  for (let attempt = 1; attempt <= ADAPTER_READ_MAX_ATTEMPTS; attempt += 1) {
    try {
      return await readFile(filePath, 'utf8');
    } catch (error) {
      if (isNodeError(error) && error.code === 'ENOENT') return null;
      if (
        !isRetriableAdapterReadError(error) ||
        attempt === ADAPTER_READ_MAX_ATTEMPTS
      ) {
        throw error;
      }
      await delay(ADAPTER_READ_RETRY_DELAY_MS * attempt);
    }
  }
  throw new Error('MANCODE_V3_ADAPTER_READ_RETRY_EXHAUSTED');
}

async function atomicWrite(filePath: string, content: string): Promise<void> {
  const temporary = path.join(
    path.dirname(filePath),
    `.${path.basename(filePath)}.${process.pid}.${Date.now()}.tmp`,
  );
  try {
    await writeFile(temporary, content, { encoding: 'utf8', flag: 'wx' });
    await rename(temporary, filePath);
  } finally {
    await rm(temporary, { force: true }).catch(() => undefined);
  }
}

