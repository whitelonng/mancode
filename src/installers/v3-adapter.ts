async function inspectManagedTarget(
  root: string,
  platform: PlatformName,
  spec: V3AdapterManagedTargetSpec,
): Promise<V3AdapterManagedTargetStatus> {
  const expectedDigest = adapterManagedContentDigest(
    spec.identity,
    spec.expectedManagedContent,
  );
  const repair = [
    `Preview with \`mancode adapter upgrade --platform ${platform} --dry-run\`.`,
    `Confirm that exact preview with \`mancode adapter upgrade --platform ${platform} --confirm --operation-id <operationId> --session <id>\`.`,
  ].join(' ');
  try {
    const bytes = await readAdapterBytesIfExists(
      root,
      v3AdapterTargetPath(root, spec.fileTarget),
    );
    if (bytes === null) {
      return managedTargetStatus(
        spec,
        'missing',
        null,
        expectedDigest,
        repair,
        'Managed target does not exist.',
      );
    }
    const content = new TextDecoder('utf-8', { fatal: true }).decode(bytes);
    let managedContent: string;
    if (spec.blockMarkers === null) {
      managedContent = content;
    } else {
      try {
        const extracted = extractManagedBlock(
          content,
          spec.blockMarkers[0],
          spec.blockMarkers[1],
        );
        if (extracted === null) {
          return managedTargetStatus(
            spec,
            'missing',
            null,
            expectedDigest,
            repair,
            'Managed block is absent from the target file.',
          );
        }
        managedContent = extracted;
      } catch (error) {
        return managedTargetStatus(
          spec,
          'stale',
          null,
          expectedDigest,
          repair,
          error instanceof Error
            ? error.message
            : 'Managed block is malformed.',
        );
      }
    }
    const actualDigest = adapterManagedContentDigest(
      spec.identity,
      managedContent,
    );
    const status = actualDigest === expectedDigest ? 'ready' : 'stale';
    return managedTargetStatus(
      spec,
      status,
      actualDigest,
      expectedDigest,
      status === 'ready' ? 'none' : repair,
      status === 'ready'
        ? 'Managed content matches the current renderer.'
        : 'Managed content differs from the current renderer.',
    );
  } catch (error) {
    return managedTargetStatus(
      spec,
      'unreadable',
      null,
      expectedDigest,
      `Resolve the filesystem or UTF-8 error, then ${repair}`,
      error instanceof Error ? error.message : 'Managed target is unreadable.',
    );
  }
}

function managedTargetStatus(
  spec: V3AdapterManagedTargetSpec,
  status: V3AdapterContentStatus,
  actualDigest: string | null,
  expectedDigest: string,
  repair: string,
  detail: string,
): V3AdapterManagedTargetStatus {
  return {
    identity: spec.identity,
    target: spec.target,
    status,
    actualDigest,
    expectedDigest,
    rendererVersion: V3_ADAPTER_VERSION,
    repair,
    detail,
  };
}

function aggregateAdapterStatus(
  targets: V3AdapterManagedTargetStatus[],
): V3AdapterContentStatus {
  if (targets.every((target) => target.status === 'ready')) return 'ready';
  if (targets.some((target) => target.status === 'unreadable')) {
    return 'unreadable';
  }
  if (targets.some((target) => target.status === 'stale')) return 'stale';
  return 'missing';
}

function relativeAdapterPath(root: string, target: string): string {
  return path.relative(root, target).split(path.sep).join('/');
}

async function writeManagedFile(
  filePath: string,
  content: string,
): Promise<void> {
  const existing = await readTextIfExists(filePath);
  if (
    existing !== null &&
    !existing.includes(V3_ADAPTER_MANAGED_MARKER) &&
    !existing.includes(LEGACY_V3_ADAPTER_MANAGED_MARKER)
  ) {
    throw new Error('MANCODE_V3_ADAPTER_TARGET_USER_AUTHORED');
  }
  await mkdir(path.dirname(filePath), { recursive: true });
  await atomicWrite(filePath, content);
}

async function writeV3ModeEntry(
  filePath: string,
  content: string,
): Promise<void> {
  const existing = await readTextIfExists(filePath);
  const managed =
    existing === null ||
    existing.includes(V3_MODE_ENTRY_MANAGED_MARKER) ||
    LEGACY_MODE_ENTRY_MANAGED_MARKERS.some((marker) =>
      existing.includes(marker),
    );
  if (!managed) {
    throw new Error('MANCODE_V3_MODE_ENTRY_USER_AUTHORED');
  }
  await mkdir(path.dirname(filePath), { recursive: true });
  await atomicWrite(filePath, content);
}

async function assertV3ModeEntriesWritable(
  root: string,
  platform: PlatformName,
): Promise<void> {
  for (const mode of V3_MODE_NAMES) {
    const existing = await readTextIfExists(
      v3ModeEntryPath(root, platform, mode),
    );
    if (
      existing !== null &&
      !existing.includes(V3_MODE_ENTRY_MANAGED_MARKER) &&
      !LEGACY_MODE_ENTRY_MANAGED_MARKERS.some((marker) =>
        existing.includes(marker),
      )
    ) {
      throw new Error('MANCODE_V3_MODE_ENTRY_USER_AUTHORED');
    }
  }
}

async function removeV3ModeEntry(filePath: string): Promise<void> {
  const existing = await readTextIfExists(filePath);
  if (
    existing?.includes(V3_MODE_ENTRY_MANAGED_MARKER) ||
    LEGACY_MODE_ENTRY_MANAGED_MARKERS.some((marker) =>
      existing?.includes(marker),
    )
  ) {
    await rm(filePath, { force: true });
    await removeDirectoryIfEmpty(path.dirname(filePath));
  }
}

async function removeDirectoryIfEmpty(directory: string): Promise<void> {
  try {
    await rmdir(directory);
  } catch (error) {
    if (
      isNodeError(error) &&
      (error.code === 'ENOENT' ||
        error.code === 'ENOTEMPTY' ||
        error.code === 'EEXIST')
    ) {
      return;
    }
    throw error;
  }
}

async function removeManagedFile(filePath: string): Promise<void> {
  const existing = await readTextIfExists(filePath);
  if (existing?.includes(V3_ADAPTER_MANAGED_MARKER)) {
    await rm(filePath, { force: true });
  }
}

async function removeRetiredBootstrapFiles(
  root: string,
  platform: PlatformName,
): Promise<void> {
  if (platform !== 'claude-code' && platform !== 'cursor') return;
  for (const retired of retiredBootstrapSpecs(root, platform)) {
    await assertAdapterPathSafe(root, retired.filePath);
    const existing = await readTextIfExists(retired.filePath);
    if (!retired.managedMarkers.some((marker) => existing?.includes(marker))) {
      continue;
    }
    await rm(retired.filePath, { force: true });
    await removeDirectoryIfEmpty(path.dirname(retired.filePath));
  }
}

async function replaceManagedV3Block(
  filePath: string,
  startMarker: string,
  endMarker: string,
  content: string,
  legacyMarkers?: readonly (readonly [string, string])[],
): Promise<void> {
  const current = (await readTextIfExists(filePath)) ?? '';
  const existing = (legacyMarkers ?? []).reduce(
    (contentWithoutLegacy, [legacyStart, legacyEnd]) =>
      removeManagedBlock(contentWithoutLegacy, legacyStart, legacyEnd),
    current,
  );
  const block = [startMarker, content, endMarker].join('\n');
  await mkdir(path.dirname(filePath), { recursive: true });
  await atomicWrite(
    filePath,
    replaceManagedBlock(existing, block, startMarker, endMarker),
  );
}

